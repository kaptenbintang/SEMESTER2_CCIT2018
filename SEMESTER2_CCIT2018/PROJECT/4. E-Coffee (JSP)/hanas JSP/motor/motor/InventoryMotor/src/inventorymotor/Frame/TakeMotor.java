    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        try {
            String id_bahan = tf_motor.getText();
            String jumlah = tf_jumlah.getText();

            Helper helper = new Helper();
            String taking = helper.parseDataToDatabase(dp_taking);

            String sql = "INSERT INTO ambil (id_motor, jumlah_motor, taking) values (?,?,?)";

            Map<Integer, Object>map = new HashMap<>();
            map.put(1, id_bahan);
            map.put(2, jumlah);
            map.put(3, taking);

            if(!controller.preparedStatement(map, sql)) {
                JOptionPane.showMessageDialog(null, "Gagal Menambahkan Data");
            }
            clearForm();
            loadTable();
            JOptionPane.showMessageDialog(null, "Berhasil Menambahkan Data");
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_btn_submitActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        try{
            Helper helper = new Helper();
            String taking = helper.parseDataToDatabase(dp_taking);

            String sql = "DELETE FROM ambil WHERE id = ? ";

            Map<Integer, Object>map = new HashMap<>();
            map.put(1, this.take_id);

            if (controller.preparedStatement(map, sql)) {
                clearForm();
                loadTable();
                JOptionPane.showMessageDialog(null, "Berhasil Menghapus Data");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal Menghapus Data");
            }
        } catch (Exception ex) {
            System.err.println(ex.getMessage());
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

