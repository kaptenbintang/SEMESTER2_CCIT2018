    private void tb_ambilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_ambilMouseClicked
        // TODO add your handling code here:
        try {
            Helper helper = new Helper();
            String id = helper.getValueRows(tb_ambil, 0);
            String id_motor = helper.getValueRows(tb_ambil, 1);
            String jumlah = helper.getValueRows(tb_ambil, 2);
            String taking = helper.getValueRows(tb_ambil, 3);
            
            btn_delete.setEnabled(true);
            
            this.take_id = id;
            tf_motor.setText(id_motor);
            tf_jumlah.setText(jumlah);
            dp_taking.setDate(new Date(helper.getParsedDate(taking)));
            
        } catch (Exception ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tb_ambilMouseClicked

